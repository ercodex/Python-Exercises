# This code explains what 'overloading' in programming is

print(3 + 5)

firstName = 'Eren'
lastName  = 'Cil'

print(firstName + lastName)

"""
Overloading is a term when you have the same operation that is used in
different ways depending on the context. In this scenario, it is 'plus sign'
"""

